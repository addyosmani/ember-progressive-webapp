/* jshint node: true */

module.exports = function(environment) {
  var ENV = {
    modulePrefix: 'ember-progressive-webapp',
    environment: environment,
    baseURL: '/',
    locationType: 'auto',
    EmberENV: {
      FEATURES: {
        // Here you can enable experimental features on an ember canary build
        // e.g. 'with-controller': true
      }
    },

    APP: {
      // Here you can pass flags/options to your application instance
      // when it is created
    }
  };

  ENV.serviceWorker = {
    enabled: true,
    debug: true,
    fallback: [
     '/offline'
    ]
  };

  if (environment === 'development') {
    // ENV.APP.LOG_RESOLVER = true;
    // ENV.APP.LOG_ACTIVE_GENERATION = true;
    // ENV.APP.LOG_TRANSITIONS = true;
    // ENV.APP.LOG_TRANSITIONS_INTERNAL = true;
    // ENV.APP.LOG_VIEW_LOOKUPS = true;
    ENV.baseURL = '';
    // ember server --environment=development
  }

  if (environment === 'fastboot-test') {
    ENV.baseURL = 'http://localhost:3000/';
    // ember fastboot --serve-assets --environment=fastboot-test
  }

  if (environment === 'test') {
    // Testem prefers this...
    ENV.baseURL = '/';
    ENV.locationType = 'none';

    // keep test console output quieter
    ENV.APP.LOG_ACTIVE_GENERATION = false;
    ENV.APP.LOG_VIEW_LOOKUPS = false;

    ENV.APP.rootElement = '#ember-testing';
  }

  if (environment === 'production') {
    // ENV.baseURL = '';
    ENV.baseURL = 'https://ember-pwa.herokuapp.com/';
  }

  return ENV;
};
